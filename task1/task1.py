x = float(input("Введіть кут радіанної міри: "))
yd = x*57.29577
deg = int(yd)
min = int((yd - deg)*60)
sec = int(((yd - deg)*60 - min)*60)
print("Кут " + str(x) + " радіан рівний " + str(deg) + " градусам " + str(min) + " хвилинам і " + str(sec) +" секундам")